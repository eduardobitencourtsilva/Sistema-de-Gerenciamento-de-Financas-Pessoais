    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed
        String despesa = tDespesa.getText();
        String tipo = tTipo.getText();
        String status = tStatus.getText(); 
        
        double valor = 0;
        
        if (!tValor.getText().equals("")) {
            try {
                valor = Double.parseDouble(tValor.getText());
            }
            
            catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erro. O campo valor precisa ser num√©rico.");
                valor = -1;
            }
        }
        
        if (!despesa.equals("") && !tipo.equals("") && !status.equals("") && valor > 0) {    
            CadastroDespesa.cadastrarDespesa(despesa, valor, tipo, "nulo", status);
         
            reorganizarTabela();
            
            JOptionPane.showMessageDialog(null, "Despesa cadastrada com sucesso!");
        }
        
        else if (valor != -1) {
            JOptionPane.showMessageDialog(null, "Falha em cadastrar a despesa.\n√â necess√°rio preencher todos os campos de texto com entradas v√°lidas.");
        }
    }//GEN-LAST:event_bCadastrarActionPerformed

